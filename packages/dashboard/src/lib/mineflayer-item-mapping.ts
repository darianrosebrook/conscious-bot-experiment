/**
 * Get display name for Mineflayer item
 */
export function getMineflayerItemDisplayName(itemType: number): string {
  const spriteName = getMineflayerItemSprite(itemType);

  // Convert snake_case to Title Case
  return spriteName
    .split('_')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}
